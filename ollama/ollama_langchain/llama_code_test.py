# write a function to return the sum of two numbers
def add(a, b):
    return a + b

# write a function to return the difference of two numbers
def subtract(a, b):
    return a - b

# write a function to return the product of two numbers
def multiply(a, b):
    return a * b

# write a function to return the quotient of two numbers
def divide(a, b):
    return a / b

# write a function that takes in a number and returns its square root
def sqrt(n):
    return n ** (1/2)

# call these functions with sample numbers
print("The sum of 3 and 4 is", add(3, 4))
print("The difference of 5 and 6 is", subtract(5, 6))
print("The product of 7 and 8 is", multiply(7, 8))
print("The quotient of 9 and 10 is", divide(9, 10))